import Foundation

/// A dynamic JSON value used to round-trip loosely-typed API fields that the
/// TypeScript source models as `Record<string, unknown>` / `any` — e.g.
/// `PlatformRecord.data`, `PlatformRecordGps.accuracyMeters`'s sibling free-form
/// fields, and other index-map shapes.
///
/// This intentionally is NOT a 1:1 port of any single TS type — it is the
/// escape hatch used wherever `shared/platformTypes.ts` declares a field as
/// `unknown`/`any`/a bare index signature, so arbitrary JSON survives an
/// encode/decode round trip without loss.
public indirect enum JSONValue: Codable, Equatable, Sendable {
    case string(String)
    case number(Double)
    case bool(Bool)
    case array([JSONValue])
    case object([String: JSONValue])
    case null

    public init(from decoder: Decoder) throws {
        let container = try decoder.singleValueContainer()
        if container.decodeNil() {
            self = .null
        } else if let bool = try? container.decode(Bool.self) {
            self = .bool(bool)
        } else if let number = try? container.decode(Double.self) {
            self = .number(number)
        } else if let string = try? container.decode(String.self) {
            self = .string(string)
        } else if let array = try? container.decode([JSONValue].self) {
            self = .array(array)
        } else if let object = try? container.decode([String: JSONValue].self) {
            self = .object(object)
        } else {
            throw DecodingError.dataCorruptedError(
                in: container,
                debugDescription: "Unsupported JSON value"
            )
        }
    }

    public func encode(to encoder: Encoder) throws {
        var container = encoder.singleValueContainer()
        switch self {
        case .string(let value):
            try container.encode(value)
        case .number(let value):
            try container.encode(value)
        case .bool(let value):
            try container.encode(value)
        case .array(let value):
            try container.encode(value)
        case .object(let value):
            try container.encode(value)
        case .null:
            try container.encodeNil()
        }
    }
}
